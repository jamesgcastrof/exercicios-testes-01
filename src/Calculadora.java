
public class Calculadora {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 5;
		
		int soma = a + b;
		int subtracao = a - b;
		int multplicacao = a * b;
		int divisao = a / b;
		
						
		System.out.println("O valor da soma é = " + soma + "\n");
		System.out.println("O valor da subtração é = " + subtracao + "\n");
		System.out.println("O valor da multiplicação é = " + multplicacao + "\n");
		System.out.println("O valor da divisão é = " + divisao);
			
	}

}
